package com.sgs.hotelguru;

public class ButtonStruct {

	private String Ship_Name;
	private int Room_Deck;
	private int Room_Num;
	private int ButtonX;
	private int ButtonY;
	public String getShip_Name() {
		return Ship_Name;
	}
	public void setShip_Name(String ship_Name) {
		Ship_Name = ship_Name;
	}
	public int getRoom_Deck() {
		return Room_Deck;
	}
	public void setRoom_Deck(int room_Deck) {
		Room_Deck = room_Deck;
	}
	public int getRoom_Num() {
		return Room_Num;
	}
	public void setRoom_Num(int room_Num) {
		Room_Num = room_Num;
	}
	public int getButtonX() {
		return ButtonX;
	}
	public void setButtonX(int buttonX) {
		ButtonX = buttonX;
	}
	public int getButtonY() {
		return ButtonY;
	}
	public void setButtonY(int buttonY) {
		ButtonY = buttonY;
	}
}
