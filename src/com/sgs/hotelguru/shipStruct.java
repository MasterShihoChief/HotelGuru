package com.sgs.hotelguru;

public class shipStruct {
	private String Ship_Name;
	private int Ship_Year;
	private String Cruise_Line;
	private int Capacity;
	private int Ship_Decks;
	private String Ship_Registry;
	private int Ship_Speed;
	private int Ship_Length;
	public String getShip_Name() {
		return Ship_Name;
	}
	public void setShip_Name(String ship_Name) {
		Ship_Name = ship_Name;
	}
	public int getShip_Year() {
		return Ship_Year;
	}
	public void setShip_Year(int ship_Year) {
		Ship_Year = ship_Year;
	}
	public String getCruise_Line() {
		return Cruise_Line;
	}
	public void setCruise_Line(String cruise_Line) {
		Cruise_Line = cruise_Line;
	}
	public int getCapacity() {
		return Capacity;
	}
	public void setCapacity(int capacity) {
		Capacity = capacity;
	}
	public int getShip_Decks() {
		return Ship_Decks;
	}
	public void setShip_Decks(int ship_Decks) {
		Ship_Decks = ship_Decks;
	}
	public String getShip_Registry() {
		return Ship_Registry;
	}
	public void setShip_Registry(String ship_Registry) {
		Ship_Registry = ship_Registry;
	}
	public int getShip_Speed() {
		return Ship_Speed;
	}
	public void setShip_Speed(int ship_Speed) {
		Ship_Speed = ship_Speed;
	}
	public int getShip_Length() {
		return Ship_Length;
	}
	public void setShip_Length(int ship_Length) {
		Ship_Length = ship_Length;
	}
}
